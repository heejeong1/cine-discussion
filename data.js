  generate: function(title, authorOrDirector, type, themeKey, toneKey, keyword) {
    const isBook = type === 'book';
    const authorLabel = isBook ? '저자' : '감독';
    const titleWithAuthor = authorOrDirector ? `[${title} (${authorOrDirector} ${authorLabel})]` : `[${title}]`;
    const finalKeyword = keyword ? keyword.trim() : (this.themes[themeKey] ? themeKey : "자아성장");
    
    // Fallback if theme or tone is not matched
    const matchedTheme = this.themes[themeKey] || this.themes["자아성장"];
    const matchedTemplates = matchedTheme[toneKey] || matchedTheme["philosophical"];
    
    return matchedTemplates.map(template => {
      let prompt = template.replaceAll("[TITLE]", titleWithAuthor);
      prompt = prompt.replaceAll("[KEYWORD]", `'${finalKeyword}'`);
      return prompt;
    });
  }
